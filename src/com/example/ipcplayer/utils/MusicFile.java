package com.example.ipcplayer.utils;

public class MusicFile {
	String MusicName;
	String ArtistName;
	String AlbumnName;
	String AlbumnImage;
	
	public String getMusicName() {
		return MusicName;
	}
	
	public void setMusicName(String musicName) {
		MusicName = musicName;
	}
	
	public String getArtistName() {
		return ArtistName;
	}
	
	public void setArtistName(String artistName) {
		ArtistName = artistName;
	}
	
	public String getAlbumnName() {
		return AlbumnName;
	}
	
	public void setAlbumnName(String albumnName) {
		AlbumnName = albumnName;
	}
	
	public String getAlbumnImage() {
		return AlbumnImage;
	}
	
	public void setAlbumnImage(String albumnImage) {
		AlbumnImage = albumnImage;
	}
	
	

}
